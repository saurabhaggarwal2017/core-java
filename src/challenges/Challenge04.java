package challenges;

public class Challenge04 {
    public static void main(String[] args) {
        // swap two numbers.

        int a = 3;
        int b = 5;
        System.out.println("value if a : "+a);
        System.out.println("value if b : "+b);

        int c = a;
        a = b;
        b = c;
        System.out.println("value if a : "+a);
        System.out.println("value if b : "+b);
    }
}
