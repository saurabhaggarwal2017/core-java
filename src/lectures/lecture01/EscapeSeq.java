package lectures.lecture01;

public class EscapeSeq {
    public static void main(String[] args) {

        // ignore double, single quotes and backslash
        System.out.println("hello \"good morning\" sir");
        System.out.println("hello \'good morning\' sir");
        System.out.println("hello \\good morning\\ sir");
        // new line
        System.out.println("hello \ngood morning sir");
    }
}
